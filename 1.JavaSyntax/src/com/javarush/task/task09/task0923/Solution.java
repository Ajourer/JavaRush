package com.javarush.task.task09.task0923;

import java.io.BufferedReader;
import java.io.InputStreamReader;
import java.util.ArrayList;
import java.util.List;

/* 
Гласные и согласные
*/

public class Solution {
    public static char[] vowels = new char[]{'а', 'я', 'у', 'ю', 'и', 'ы', 'э', 'е', 'о', 'ё'};

    public static void main(String[] args) throws Exception {
        BufferedReader reader = new BufferedReader(new InputStreamReader(System.in));
        StringBuilder vowelsStr = new StringBuilder();
        StringBuilder notVowelsStr = new StringBuilder();

        for (char c : reader.readLine().replace(" ", "").toCharArray()) {
            if (isVowel(c)) {
                vowelsStr.append(c).append(" ");
            } else {
                notVowelsStr.append(c).append(" ");
            }
        }
        System.out.println(vowelsStr.append("\n").append(notVowelsStr));
    }

    // метод проверяет, гласная ли буква
    public static boolean isVowel(char character) {
        character = Character.toLowerCase(character);  // приводим символ в нижний регистр - от заглавных к строчным буквам
        for (char vowel : vowels) {  // ищем среди массива гласных
            if (character == vowel) {
                return true;
            }
        }
        return false;
    }
}